namespace LightsOut
{
    /// <summary>
    /// Enum to indicate in what state a <seealso cref="Light"/> is in.
    /// </summary>
    public enum LightState
    {
        /// <summary>Indicates that this <seealso cref="Light"/> is in the Off state.</summary>
        Off = 0,
        /// <summary>Indicates that this <seealso cref="Light"/> is in the On state.</summary>
        On = 1
    }
}
